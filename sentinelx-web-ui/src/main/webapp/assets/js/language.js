// Get current language from localStorage or default to 'en'
let currentLang = localStorage.getItem('sentinelx-lang') || 'en';

// Function to get translation
function t(key) {
    return translations[currentLang][key] || key;
}

// Function to change language
function changeLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('sentinelx-lang', lang);
    updatePageLanguage();
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
    
    // Close language dropdown if exists
    const langDropdown = document.getElementById('language-dropdown');
    if(langDropdown) langDropdown.classList.add('hidden');
}

// Function to update all text on the page
function updatePageLanguage() {
    const trans = translations[currentLang];
    
    // Update current language display
    const currentLangEl = document.getElementById('current-lang');
    if(currentLangEl) currentLangEl.textContent = trans.currentLang;
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (trans[key]) {
            // Check if it's an input placeholder
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = trans[key];
            } else {
                el.textContent = trans[key];
            }
        }
    });
    
    // Update page title
    const titleKey = document.body.getAttribute('data-page-title');
    if(titleKey && trans[titleKey]) {
        document.title = `${trans[titleKey]} - ${trans.brandName} ${trans.brandTagline}`;
    }
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLang;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    updatePageLanguage();
});

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { translations, changeLanguage, updatePageLanguage, t };
}
